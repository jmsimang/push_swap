#include "../includes/libft.h"

void	ft_wdcopy(char *str, char const *s, char c, int *i)
{
	while (*s == c && *s)
	{
		s++;
		(*i)++;
	}
	while (*s != c && *s)
	{
		*str++ = *s++;
		(*i)++;
	}
}
